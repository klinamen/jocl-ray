package io.klinamen.joclray.samples;

import io.klinamen.joclray.geom.Box;
import io.klinamen.joclray.geom.Plane;
import io.klinamen.joclray.geom.Sphere;
import io.klinamen.joclray.light.PointLight;
import io.klinamen.joclray.light.SpotLight;
import io.klinamen.joclray.scene.Camera;
import io.klinamen.joclray.scene.Scene;
import io.klinamen.joclray.util.FloatVec4;

public class Scene2 {
    public static Scene build() {
        return new Scene(new Camera()
                .setFovGrad(30)
        )
                .setAmbientLightIntensity(0.1f)
                .add(new PointLight()
                        .setIntensity(0.5f)
                        .setPosition(new FloatVec4(-12, 15, -10))
                )
                .add(new SpotLight()
                        .setIntensity(2.5f)
                        .setPosition(new FloatVec4(4, 8, 0))
                        .setAngleGrad(30)
                        .setDirection(new FloatVec4(-0.4f, -0.4f, -1))
                )
                .add(new SpotLight()
                        .setIntensity(2.5f)
                        .setPosition(new FloatVec4(-4, 8, 0))
                        .setAngleGrad(30)
                        .setDirection(new FloatVec4(0.4f, -0.4f, -1))
                )
                .add("Blue_Sphere", new Sphere()
                        .setCenter(new FloatVec4(6, -2, -25))
                        .setRadius(3)
                        .setKd(new FloatVec4(0, 0f, 0.5f))
                        .setKs(new FloatVec4(0.5f, 0.5f, 0.5f))
                        .setKr(new FloatVec4(0.4f, 0.4f, 0.2f))
                        .setPhongExp(800)
                )
                .add("Step_1", new Box()
                        .setVertexMin(new FloatVec4(-15f, -5, 0))
                        .setVertexMax(new FloatVec4(-5f, -2, -50))
                        .setKd(new FloatVec4(0.4f, 0.5f, 0.5f))
                        .setPhongExp(1000)
                )
                .add("Step_2", new Box()
                        .setVertexMin(new FloatVec4(-1, -5, 0))
                        .setVertexMax(new FloatVec4(-5, -3, -50))
                        .setKd(new FloatVec4(0.4f, 0.5f, 0.5f))
                        .setPhongExp(1000)
                )
                .add("Step_3", new Box()
                        .setVertexMin(new FloatVec4(3f, -5, 0))
                        .setVertexMax(new FloatVec4(-1, -4, -50))
                        .setKd(new FloatVec4(0.4f, 0.5f, 0.5f))
                        .setPhongExp(1000)
                )
                .add("Red_Sphere", new Sphere()
                        .setCenter(new FloatVec4(-4.5f, 0, -20))
                        .setRadius(2)
                        .setKd(new FloatVec4(0.5f, 0f, 0f))
                        .setKs(new FloatVec4(0.5f, 0.5f, 0.5f))
                        .setKr(new FloatVec4(0.2f, 0.4f, 0.4f))
                        .setPhongExp(800)
                )

                .add("Ceiling", new Plane()
                        .setNormal(new FloatVec4(0, -1, 0))
                        .setPosition(new FloatVec4(0, 20, 0))
                        .setKd(new FloatVec4(0.752f, 0.901f, 0.925f))
                )
                .add("Floor", new Plane()
                        .setNormal(new FloatVec4(0, 1, 0))
                        .setPosition(new FloatVec4(0, -5, 0))
                        .setKd(new FloatVec4(0.3f, 0.2f, 0.3f))
                        .setKs(new FloatVec4(0.3f, 0.3f, 0.3f))
                        .setKr(new FloatVec4(0.2f, 0.2f, 0.2f))
                        .setPhongExp(10)
                )
                .add("Back_Wall", new Plane()
                        .setNormal(new FloatVec4(0, 0, 1))
                        .setPosition(new FloatVec4(0, 0, -50))
                        .setKd(new FloatVec4(0.3f, 0.1f, 0.1f))
                        .setKr(new FloatVec4(0.3f, 0.1f, 0.1f))
                        .setPhongExp(1000)
                )
                .add("Left_Wall", new Plane()
                        .setNormal(new FloatVec4(1, 0, 0))
                        .setPosition(new FloatVec4(-15, 0, 0))
                        .setKd(new FloatVec4(0.1f, 0.1f, 0.3f))
                        .setPhongExp(1000)
                )
                .add("Right_Wall", new Plane()
                        .setNormal(new FloatVec4(-1, 0, 0))
                        .setPosition(new FloatVec4(30, 0, 0))
                        .setKd(new FloatVec4(0.1f, 0.3f, 0.1f))
                        .setPhongExp(1000)
                )
                ;
    }
}
